package com.example.StudentManagementSystemTask1;

public class Student {
    public int studentId;
    public String name;
    public  int rollNum;
    public int age;
    public String gender;

    public Student(int studentId, String name, int rollNum, int age, String gender) {
        this.studentId = studentId;
        this.name = name;
        this.rollNum = rollNum;
        this.age = age;
        this.gender = gender;
    }
}
